export const COMPUTE_PERCENT = (value: number, min: number, max: number): number => {
  let currentValue = value;
  if (value < min) {
    currentValue = min;
  }
  if (value > max) {
    currentValue = max;
  }
  const range = max - min;
  return range <= 0 ? 0 : Math.min(Math.max(((currentValue - min) / range) * 100, 0), 100);
}
export const GET_COLOR = (percent: number, colors: string[]): string => {
  if (!colors || colors.length === 0) return '#000000';
  const sanitizedPercent = Math.max(0, Math.min(100, percent));
  const index = Math.round((sanitizedPercent / 100) * (colors.length - 1));
  return colors[index];
}
